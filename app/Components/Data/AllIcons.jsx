export default function AllIcons() {
  const {
    allIconsData,
    setAllIconsData,
    selectedIcon,
    setSelectedIcon,
    openIconWindow,
    setOpenIconWindow,
  } = useContextApp();
  function handleTheIconSelection(singleIcon) {
    setAllIconsData((prevIcons) =>
      prevIcons.map((icon) => {
        if (icon.name === singleIcon.name) {
          setSelectedIcon(singleIcon);
          return { ...icon, isSelected: true };
        }
        return { ...icon, isSelected: false };
      })
    );
    setOpenIconWindow(false);
  }
  return (
    <div className="flex flex-wrap gap-2 text-green-500 p-3">
      {allIconsData.map((singleIcon, index) => (
        <div
          key={index}
          onClick={() => handleTheIconSelection(singleIcon)}
          className={`w-9 h-9 shadow-lg hover:bg-green-200 hover:text-green-700 border cursor-pointer border-slate-50 flex items-center justify-center rounded-lg ${
            singleIcon.isSelected
              ? "bg-green-200 text-green-700"
              : "bg-white text-green-500"
          }`}
        >
          {singleIcon.icon}
        </div>
      ))}
    </div>
  );
}
